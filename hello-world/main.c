// This is a comment
// any file with .h extention is called a header file
// We include different header files at top of our code
// Header files allow us to add extra functionality i.e. to add code someone else already did to our program
#include <stdio.h>

// Main is the entry point of our code, everything our program does should be written inside main
int main()
{
	// printf is a function provided by including stdio.h,
	// it allows us to print to the standard output aka terminal
	printf("Hello world!\n");
	return 0;
}
